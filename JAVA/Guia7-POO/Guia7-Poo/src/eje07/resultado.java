public class resultado {
    int mayores;
    int menores;
    int bajoPeso;
    int pesoIdeal;
    int sobrePeso;

    public resultado() {
    }

    public resultado(int mayores, int menores, int bajoPeso, int pesoIdeal, int sobrePeso) {
        this.mayores = mayores;
        this.menores = menores;
        this.bajoPeso = bajoPeso;
        this.pesoIdeal = pesoIdeal;
        this.sobrePeso = sobrePeso;
    }
    
    
}
